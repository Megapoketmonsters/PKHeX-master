        {
            int gt = (int)SpeciesTable().Rows[species][8];
            uint pid = (uint)Util.rnd32();
            if (gt > 255)
                return pid;
            while (true) // Loop until we find a suitable PID
            {
                uint gv = (pid & 0xFF);
                if (cg == 2) // Genderless
                    break;  // PID Passes
                else if ((cg == 1) && (gv < gt)) // Female
                    break;  // PID Passes
                else if ((cg == 0) && (gv >= gt))
                    break;  // PID Passes
                pid = (uint)Util.rnd32();
            }
            return pid;
        }

